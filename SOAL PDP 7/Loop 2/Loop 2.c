#include <stdio.h>
#include <stdlib.h>

int main()
{
    int b = 1;
    while(b<=7){//Sebuah iterator akan berubah nilai nya agar mencapai batas dimana perulangan akan berhenti
        printf("Perulangan ke %d\n",b);
        b++;//Sebuah perulangan memerlukan objek yang menjadi acuan berjalannya proses perulangan
    }



    puts("\n");
    return 0;
}
